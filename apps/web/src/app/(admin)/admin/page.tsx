'use client';

import { useEffect, useState } from 'react';
import { adminApi, AdminStats } from '@/lib/api';

export default function AdminDashboardPage() {
  const [stats, setStats] = useState<AdminStats | null>(null);
  const [isLoading, setIsLoading] = useState(true);
  const [error, setError] = useState<string | null>(null);

  useEffect(() => {
    const fetchStats = async () => {
      try {
        const { data } = await adminApi.getStats();
        setStats(data);
      } catch (err) {
        setError('Failed to load stats');
        console.error(err);
      } finally {
        setIsLoading(false);
      }
    };

    fetchStats();
  }, []);

  if (isLoading) {
    return (
      <div className="flex items-center justify-center h-64">
        <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-red-600" />
      </div>
    );
  }

  if (error) {
    return (
      <div className="p-4 rounded-lg bg-red-900/50 border border-red-700">
        <p className="text-red-400">{error}</p>
      </div>
    );
  }

  const statCards = [
    {
      name: 'Total Tenants',
      value: stats?.totalTenants ?? 0,
      icon: BuildingIcon,
      color: 'bg-blue-600',
    },
    {
      name: 'Active Tenants',
      value: stats?.activeTenants ?? 0,
      icon: CheckIcon,
      color: 'bg-green-600',
    },
    {
      name: 'Total Users',
      value: stats?.totalUsers ?? 0,
      icon: UsersIcon,
      color: 'bg-purple-600',
    },
    {
      name: 'Tenants This Month',
      value: stats?.tenantsThisMonth ?? 0,
      icon: TrendingUpIcon,
      color: 'bg-orange-600',
    },
    {
      name: 'Users This Month',
      value: stats?.usersThisMonth ?? 0,
      icon: UserPlusIcon,
      color: 'bg-pink-600',
    },
  ];

  return (
    <div className="space-y-6">
      {/* Header */}
      <div>
        <h1 className="text-2xl font-bold text-white">Dashboard</h1>
        <p className="text-gray-400 mt-1">Overview of your SaaS platform</p>
      </div>

      {/* Stats Grid */}
      <div className="grid grid-cols-1 md:grid-cols-2 lg:grid-cols-3 xl:grid-cols-5 gap-4">
        {statCards.map((stat) => (
          <div
            key={stat.name}
            className="bg-gray-900 rounded-lg border border-gray-800 p-6"
          >
            <div className="flex items-center gap-4">
              <div className={`p-3 rounded-lg ${stat.color}`}>
                <stat.icon className="w-6 h-6 text-white" />
              </div>
              <div>
                <p className="text-sm text-gray-400">{stat.name}</p>
                <p className="text-2xl font-bold text-white">{stat.value}</p>
              </div>
            </div>
          </div>
        ))}
      </div>

      {/* Quick Actions */}
      <div className="bg-gray-900 rounded-lg border border-gray-800 p-6">
        <h2 className="text-lg font-semibold text-white mb-4">Quick Actions</h2>
        <div className="flex flex-wrap gap-4">
          <a
            href="/admin/tenants"
            className="px-4 py-2 bg-gray-800 hover:bg-gray-700 text-white rounded-md transition-colors"
          >
            Manage Tenants
          </a>
          <a
            href="/admin/users"
            className="px-4 py-2 bg-gray-800 hover:bg-gray-700 text-white rounded-md transition-colors"
          >
            Manage Users
          </a>
        </div>
      </div>
    </div>
  );
}

